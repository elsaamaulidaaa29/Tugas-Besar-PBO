    private void btnSelesaiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSelesaiActionPerformed
        // TODO add your handling code here:
        if (fieldNo.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Pilih tugas yang mau dihapus", "Pesan",JOptionPane.WARNING_MESSAGE);
        } else {
            try {
                koneksi = DriverManager.getConnection("jdbc:mysql://localhost/db_notes", "root", "");
                String no = fieldNo.getText();
                String Query = "Delete from tbl_tugas where no="+no;
                Statement Delete = koneksi.createStatement();
                Delete.executeUpdate(Query);
                JOptionPane.showMessageDialog(this, "Tugasmu udah selesai");
                tampilDataNow();

            } catch (Exception e) {
                e.printStackTrace();
            }
        }
        
        
    }//GEN-LAST:event_btnSelesaiActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        if (fieldNo.getText().isEmpty() || fieldTugas.getText().isEmpty() || fieldKategori.getSelectedIndex() == -1){
            JOptionPane.showMessageDialog(this, "Pilih tugas yang mau diupdate", "Pesan",JOptionPane.WARNING_MESSAGE);
        } else {
            try {
                koneksi = DriverManager.getConnection("jdbc:mysql://localhost/db_notes", "root", "");
                String no = fieldNo.getText();
                String Query = "update tbl_tugas set tugas='"+fieldTugas.getText()+"', kategori='"+fieldKategori.getSelectedItem()+"', deskripsi='"+fieldDeskripsi.getText()+"' where no="+no;
                Statement Update = koneksi.createStatement();
                Update.executeUpdate(Query);
                JOptionPane.showMessageDialog(this, "Tugasmu diupdate");
                tampilDataNow();
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
        
    }//GEN-LAST:event_btnEditActionPerformed

